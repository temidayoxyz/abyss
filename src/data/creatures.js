export function getSpeciesForDepth(depth) {
  return SPECIES.filter((s) => depth >= s.depthMin && depth <= s.depthMax);
}

export function getSpeciesById(id) {
  return SPECIES.find((s) => s.id === id) || LANDMARK_TYPES.find((s) => s.id === id);
}

export function createProfile(species, extras = {}) {
  const rarity = RARITY[species.rarity] || RARITY.common;
  return {
    id: species.id,
    name: species.name,
    type: species.type || species.category || "unknown",
    depthRange: `${species.depthMin}–${species.depthMax} m`,
    depthMin: species.depthMin,
    depthMax: species.depthMax,
    behaviour: species.behaviour,
    rarity: rarity.id,
    rarityLabel: rarity.label,
    facts: species.facts || [],
    scannedAt: extras.scannedAt || Date.now(),
    depthWhenFound: extras.depthWhenFound ?? null,
    isLandmark: Boolean(species.category === "landmark"),
    photoCount: extras.photoCount || 0,
    sampleCount: extras.sampleCount || 0,
  };
}
